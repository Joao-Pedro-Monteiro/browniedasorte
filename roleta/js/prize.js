let position = 0;

function readStorageArray(key) {
  const rawValue = localStorage.getItem(key);

  if (!rawValue) {
    return [];
  }

  try {
    const parsedValue = JSON.parse(rawValue);
    return Array.isArray(parsedValue) ? parsedValue : [];
  } catch (error) {
    console.error(`[Prize] Falha ao recuperar dados da chave ${key}.`, error);
    return [];
  }
}

function saveStorageArray(key, value) {
  localStorage.setItem(key, JSON.stringify(value));
}

function logPrizeState(message, extra = {}) {
  console.log(`[Prize] ${message}`, {
    position,
    listLength: list.length,
    ...extra
  });
}

function savePrizeState() {
  localStorage.setItem(STORAGE_KEYS.list, JSON.stringify(list));
  localStorage.setItem(STORAGE_KEYS.position, String(position));
  logPrizeState("Estado salvo no localStorage");
}

function clearPrizeState() {
  localStorage.removeItem(STORAGE_KEYS.list);
  localStorage.removeItem(STORAGE_KEYS.position);
  list = [];
  position = 0;
  logPrizeState("Estado limpo");
}

function setList(forceNew = false) {
  if (!forceNew && Array.isArray(list) && list.length > 0) {
    logPrizeState("Mantendo lista atual");
    return list;
  }

  position = 0;
  savePrizeState();
  return list;
}

function restorePrizeState() {
  const savedList = localStorage.getItem(STORAGE_KEYS.list);
  const savedPosition = Number.parseInt(localStorage.getItem(STORAGE_KEYS.position), 10);

  if (savedList) {
    try {
      const parsedList = JSON.parse(savedList);
      if (Array.isArray(parsedList) && parsedList.length > 0) {
        list = parsedList;
        position = Number.isInteger(savedPosition) && savedPosition >= 0 ? savedPosition : 0;

        if (position >= list.length) {
          logPrizeState("angle recuperada era invalida, resetando para 0", {
            recoveredPosition: position
          });
          position = 0;
          savePrizeState();
        }

        logPrizeState("Estado restaurado do localStorage");
        return;
      }
    } catch (error) {
      console.error("[Prize] Falha ao recuperar lista salva.", error);
    }
  }

  logPrizeState("Nenhum estado valido encontrado, sorteando nova lista");
  setList(true);
}

function getCurrentPrizeEntry() {
  if (!Array.isArray(list) || list.length === 0) {
    restorePrizeState();
  }

  if (!Array.isArray(list) || list.length === 0) {
    setList(true);
  }

  if (!Number.isInteger(position) || position < 0 || position >= list.length) {
    logPrizeState("Position atual invalida, resetando", { invalidPosition: position });
    position = 0;
    savePrizeState();
  }

  const prizeCode = list[position].code;
  const config = PRIZE_MAP[prizeCode];

  if (!config) {
    console.error("[Prize] Premio desconhecido encontrado na lista.", {
      prizeCode,
      position,
    });
    throw new Error(`Premio desconhecido: ${prizeCode}`);
  }

  const angleIndex = Math.floor(Math.random() * config.angles.length);
  const angle = config.angles[angleIndex];
  const result = {
    code: config.code,
    label: config.label,
    angle,
    position,
  };

  position += 1;

  if (position >= list.length) {
    logPrizeState("Fim da tabela atingido, nova lista sera preparada apos este sorteio", {
      deliveredPrize: result
    });
    setList(true);
  } else {
    savePrizeState();
  }

  logPrizeState("Premio entregue", result);
  return result;
}

function controller() {
  restorePrizeState();
  logPrizeState("Controlador inicializado");
  return {
    position,
    listLength: list.length
  };
}

function addUserToHistory(username) {
  const trimmedUsername = typeof username === "string" ? username.trim() : "";

  if(trimmedUsername.toLowerCase() === 'teste'){
    return []; //* Ignora entradas de teste para manter o historico limpo
  } 
  
  if (!trimmedUsername) {
    return [];
  }

  const usersHistory = readStorageArray(STORAGE_KEYS.users);
  const updatedHistory = [
    ...usersHistory,
    {
      username: trimmedUsername,
      registeredAt: new Date().toISOString()
    }
  ];

  saveStorageArray(STORAGE_KEYS.users, updatedHistory);
  logPrizeState("Usuario adicionado ao historico", {
    username: trimmedUsername,
    totalUsers: updatedHistory.length
  });
  return updatedHistory;
}

function addSpinToHistory(username, prizeResult) {
  const trimmedUsername = typeof username === "string" ? username.trim() : "";

  if(trimmedUsername.toLowerCase() === 'teste'){
    console.log("Entrada de teste detectada, ignorando registro no historico", { username });
    return []; //* Ignora entradas de teste para manter o historico limpo
  }

  if (!trimmedUsername || !prizeResult || !prizeResult.code) {
    return [];
  }

  const spinHistory = readStorageArray(STORAGE_KEYS.spinHistory);
  const updatedHistory = [
    ...spinHistory,
    {
      username: trimmedUsername,
      prizeCode: prizeResult.code,
      prizeLabel: prizeResult.label,
      offerAccepted: false,
      position: prizeResult.position + 1, // Armazena a posição real (1-based) para facilitar leitura
      positionCode: list[prizeResult.position].position_code,
      spinTime: new Date().toISOString()
    }
  ];

  saveStorageArray(STORAGE_KEYS.spinHistory, updatedHistory);
  logPrizeState("Sorteio registrado no historico", {
    username: trimmedUsername,
    prizeCode: prizeResult.code,
    totalSpins: updatedHistory.length
  });

  //! SALVA RESULTADO DO SORTEIO E CLIENTE NO BANCO DE DADOS
  let participantData = JSON.parse(sessionStorage.getItem("brownie_participant"))
  if(participantData){
    try{
      db_update('brownieCode', {code: participantData.codigo}, {used: true, client_name: participantData.nome, client_doc: participantData.cpf, client_tel: participantData.telefone, usedAt: new Date().toISOString(), prizeCode: prizeResult.code, position_code: list[prizeResult.position].position_code});
    } catch(error){
      console.error("Erro ao atualizar código do cliente no banco de dados", error);
    }
  }
  
  
  return updatedHistory;
}

